#include <stdio.h>

void    equation(int n)
{
	int		a;
	int		b;
	int		c;

	a = 0;
	while (a < 10)
	{
    	b = 0;
    		while (b < 10)
    		{
    			c = 0;
    			while (c < 10)
    			{
					if (11 * a + 10 * c + b == n)
						printf("A = %d, B = %d, C = %d\n", a, b, c);
        			++c;
    			}
       			++b;
		    }
		++a;
	}
}
